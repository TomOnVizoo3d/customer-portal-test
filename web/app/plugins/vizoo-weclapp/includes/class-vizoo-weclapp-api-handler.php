    /**
     * Creates a quotation in weclapp.
     *
     * @since     1.0.0
     * @access    public
     * @param     string     $deal_type           ID for the type of the deal.
     * @param     string     $license_key         The license key.
     * @param     integer    $from_timestamp      dd
     * @param     integer    $to_timestamp        dd
     * @param     string     $customer_number     The weclapp customer number.
     * @param     string     $article_number      The weclapp article number.
     * @param     string     $discount            The discount percentage as string (without percent sign).
     * @param     string     $quantity            The quantity (=years of maintenance requested) as string.
     * @param     string     $tax_id              The id for the tax class.
     * @param     string     $unit_price          The price for a year of maintenance.
     * @param     array      $address             Address array used as delivery and invoice address.
     * @param     string     $mail_address        The customer's email address.
     * @param     string     $currency            Currency of the deal. Accepts a 3-character currency code (e.g. EUR, USD).
     * @param     string     $responsible_user    ID of the responsible user (Vizoo license manager).
     * @return    string                          The ID of the newly created quotation.
     */
    public static function create_quotation($deal_type, $license_key, $from_timestamp, $to_timestamp, $customer_number, $article_number, $discount, $quantity, $tax_id, $unit_price, $address, $mail_address, $currency_code, $responsible_user, &$quotation_number)
    {
        if (!is_numeric($customer_number)) {
            throw new \Exception("Invalid customer number. Not a number. Value: " . $customer_number);
        }
        $customer_party = self::get_party_from_customer_number($customer_number);
        $article = self::get_artice_from_article_number($article_number);
        $currency = self::get_currency_id_from_currency_code($currency_code);
        $args = [
            'customAttributes' => [
                [
                    'attributeDefinitionId' => '89868',
                    'selectedValueId' => $deal_type,
                ],
                [
                    'attributeDefinitionId' => '164663',
                    'stringValue' => '(auto-generated by maintenance and subscription workflow)',
                ],
            ],
            'customerId' => $customer_party['id'],
            'quotationDate' => (int)(new DateTimeImmutable())->format('Uv'),
            'quotationItems' => [
                [
                    'articleId' => $article['id'],
                    'discountPercentage' => $discount,
                    'manualQuantity' => true,
                    'manualUnitPrice' => true,
                    'quantity' => $quantity,
                    'taxId' => $tax_id,
                    'unitPrice' => $unit_price,
                    'customAttributes' => [
                        [
                            'attributeDefinitionId' => '72289',
                            'stringValue' => $license_key,
                        ],
                        [
                            'attributeDefinitionId' => '73006',
                            'dateValue' => $from_timestamp,
                        ],
                        [
                            'attributeDefinitionId' => '73018',
                            'dateValue' => $to_timestamp,
                        ],
                    ],
                ],
            ],
            'quotationType' => 'SALES_ORDER',
            'recordAddress' => $address,
            'recordEmailAddresses' => [
                'toAddresses' => $mail_address,
            ],
            'invoiceAddress' => $address,
            'deliveryAddress' => $address,
            'deliveryEmailAddresses' => [
                'toAddresses' => $mail_address,
            ],
            'recordCurrencyId' => $currency['id'],
            'salesInvoiceEmailAddresses' => [
                'toAddresses' => $mail_address,
            ],
        ];
        if ($responsible_user) {
            $args['responsibleUserId'] = $responsible_user;
        }
        $response = self::call_api('POST', 'quotation', array_filter($args));

        $quotation_number = $response['quotationNumber'];
        return $response['id'];
    }

