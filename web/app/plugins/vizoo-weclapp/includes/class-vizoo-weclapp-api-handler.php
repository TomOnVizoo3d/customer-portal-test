    public static function create_quotation_pdf($quotation_id)
    {
        return self::call_api('POST', sprintf('quotation/id/%d/createQuotationPdf', $quotation_id), [], [], false);
    }

    private static function get_party_from_customer_number($customer_number)
    {
        $query_args = [
            'customerNumber-eq' => $customer_number,
        ];
        $response =  self::call_api('GET', 'party', [], $query_args);
        if (empty($response['result']) || !isset($response['result'][0]['id'])) {
            throw new RuntimeException("Unable to find party for customer number: " . $customer_number);
        }
        return $response['result'][0];
    }

    private static function get_artice_from_article_number($article_number)
    {
        $query_args = [
            'articleNumber-eq' => $article_number,
        ];
        $response = self::call_api('GET', 'article', [], $query_args);
        if (empty($response['result']) || !isset($response['result'][0]['id'])) {
            throw new RuntimeException("Unable to find article for article number: " . $article_number);
        }
        return $response['result'][0];
    }

    private static function get_currency_id_from_currency_code($currency_code)
    {
        $query_args = [
            'name-eq' => $currency_code,
        ];
        $response = self::call_api('GET', 'currency', [], $query_args);
        if (empty($response['result']) || !isset($response['result'][0]['id'])) {
            throw new RuntimeException("Unable to find currency id for currency code: " . $currency_code);
        }
        return $response['result'][0];
    }

    /**
     * Calls the weclapp API.
     *
     * @since     1.0.0
     * @access    private
     * @param     string     $method        The HTTP request method.
     * @param     string     $uri           Resource identifier.
     * @param     array      $postfields    Request body data.
     * @param     array      $query_args    Arguments to be added to the URL as query.
     * @return    array                     The data that is returned by the weclapp API.
     */
    private static function call_api($method, $uri, $postfields = [], $query_args = [], $parse_result = true)
    {
        $url = getenv('VIZOO_WECLAPP_API_ADDRESS') . $uri;

        if (!empty($query_args)) {
            $url .= '?' . http_build_query($query_args);
        }

        if (empty($postfields)) {
            $postfields = (object)$postfields;
        }

        $channel = curl_init();
        curl_setopt($channel, CURLOPT_ENCODING, 'gzip');
        curl_setopt($channel, CURLOPT_URL, $url);
        curl_setopt($channel, CURLOPT_RETURNTRANSFER, true);
        curl_setopt($channel, CURLOPT_CUSTOMREQUEST, $method);
        curl_setopt($channel, CURLOPT_HTTPHEADER, [
            'Content-Type: application/json',
            'Accept: application/json',
            sprintf('AuthenticationToken: %s', getenv('VIZOO_WECLAPP_API_TOKEN')),
        ]);
        if ($method === 'POST' || $method === 'PUT') {
            curl_setopt($channel, CURLOPT_POSTFIELDS, json_encode($postfields));
        }
        $output = curl_exec($channel);

        $error_code = curl_errno($channel);
        $response_code = curl_getinfo($channel, CURLINFO_RESPONSE_CODE);

        curl_close($channel);
        if ($error_code !== 0) {
            throw new RuntimeException("The request to the weclapp API failed with error code $error_code.");
        }
        if ($response_code > 299) {
            throw new RuntimeException("The weclapp API returned HTTP status code $response_code.");
        }

        if (!$parse_result) {
            return $output;
        }
        return json_decode($output, true);
    }
}
